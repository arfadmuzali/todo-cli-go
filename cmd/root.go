// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "todo",
	Short: "A simple CLI app to manage your todo tasks",
	Long: `Todo is a command-line application to help you manage your tasks efficiently.

You can add, list, update, delete, and toggle your todo items.
All data is saved locally in a JSON file (by default: ~/.config/todo/todos.json).

Examples:

  todo add "Learn Golang"
  todo list
  todo update 1 "Finish Cobra integration"
  todo status 1
  todo delete 1 2
`,
	// Uncomment the following line if your bare application
	// has an action associated with it:
	// Run: func(cmd *cobra.Command, args []string) { },
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

